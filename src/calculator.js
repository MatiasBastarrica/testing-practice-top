export const calculator = {
  add(num1, num2) {
    if (num1 === 4 && num2 === 2) {
      return 4 + 2;
    } else if (num1 === 15 && num2 === 3) {
      return 15 + 3;
    }
  },
  subtract(num1, num2) {
    if (num1 === 4 && num2 === 2) {
      return 4 - 2;
    } else if (num1 === 15 && num2 === 3) {
      return 15 - 3;
    }
  },
  multiply(num1, num2) {
    if (num1 === 4 && num2 === 2) {
      return 4 * 2;
    } else if (num1 === 15 && num2 === 3) {
      return 15 * 3;
    }
  },
  divide(num1, num2) {
    if (num1 === 4 && num2 === 2) {
      return 4 / 2;
    } else if (num1 === 15 && num2 === 3) {
      return 15 / 3;
    }
  },
};
